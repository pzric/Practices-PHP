    <p>PASO 1. Identifique, indique y clarifique el problema</p>
    <p>Este paso implica una fase de identificación de los diversos problemas que
    llevan a la selección de un tema principal, que será el que vayamos a resolver (el problema seleccionado puede ser el más
    importante para el grupo de personas/organizaciones involucradas o puede ser seleccionado de acuerdo con la experiencia del
    grupo que se ocupa del mismo. Implica además una clara definición de cual es el problema a tratar estableciendo qué puede estar
    causándolo y dónde están las dificultades, decidiendo sobre los puntos principales del mismo.</p>

    <p>PASO 2. Analice el problema recopilando hechos e información</p>
    <p>Implica la recopilación de la información necesaria para trabajar en el problema a fin de familiarizarse con
    todas las causas posibles.</p>

    <p>PASO 3. Desarrolle soluciones alternativas</p>
    <p>Implica la enumeración y el examen de todas las diferentes formas para solucionar un problema y
    un análisis del impacto tanto positivo como negativo de cada solución alternativa.</p>

    <p>PASO 4. Seleccione la mejor solución</p>
    <p>Este paso consiste en el proceso de seleccionar la mejor o mejores soluciones alternativas del grupo de soluciones posibles
    que hemos encontrado. La alternativa ideal puede no ser la mejor en ese momento. La mejor alternativa se determina con base
    no solamente en su eficiencia, o porque es la solución más rápida, sino además en consideración de factores tales como el
    presupuesto y tiempo disponibles, la situación económica, la capacidad del personal para ejecutarla, etc.</p>

    <p>PASO 5. Diseñe un plan de acción</p>
    <p>Implica definir una meta, delineando los objetivos y tareas específicas, el programa y presupuesto, las
    responsabilidades, etc.</p>

    <p>PASO 6. Implemente una solución</p>
    <p>Implica poner la solución escogida en ejecución y supervisar su progreso así como el seguimiento correcto del plan del proyecto.</p>

    <p>PASO 7. Evalúe</p>
    <p>Este paso implica la evaluación sobre cómo hemos alcanzado nuestro objetivo, determinando los efectos o ramificaciones de la
    solución y las implicaciones positivas y negativas del proceso/fases del proyecto respecto a nuestra idea y objetivos iniciales.</p>
